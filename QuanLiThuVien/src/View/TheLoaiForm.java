    private void loadData() {
        DefaultTableModel model = (DefaultTableModel) tblList.getModel();
        model.setRowCount(0);
        List<TheLoai> list = theLoaiService.getTheLoais();
        for (TheLoai theLoai : list) {
            Object rowData[] = {theLoai.getID(), theLoai.getMaTheLoai(), theLoai.getTenTheLoai()};
            model.addRow(rowData);
        }
    }

    private void addData() {
        TheLoai theLoai = new TheLoai();
        theLoai.setMaTheLoai(txtMaTL.getText());
        theLoai.setTenTheLoai(txtTenTL.getText());
        if (theLoaiService.add(theLoai)) {
            JOptionPane.showMessageDialog(this, "add thanh cong");
        } else {
            JOptionPane.showMessageDialog(this, "add that bai");
        }
    }

    private void loadPage(int PageNo, int step) {
        List<TheLoai> list = theLoaiService.getListPage(PageNo, step);
        DefaultTableModel model = (DefaultTableModel) tblList.getModel();
        model.setRowCount(0);
        for (TheLoai theLoai : list) {
            Object rowData[] = {theLoai.getID(), theLoai.getMaTheLoai(), theLoai.getTenTheLoai()};
            model.addRow(rowData);
        }
    }
    private void clearField() {
        txtMaTL.setText("");
        txtTenTL.setText("");
        txtTimKiem.setText("");
    }
