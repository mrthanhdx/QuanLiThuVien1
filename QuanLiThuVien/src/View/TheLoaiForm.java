    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        if (!txtMaTL.getText().isEmpty() && !txtTenTL.getText().isEmpty()) {
            if (txtMaTL.getText().length() > 10) {
                JOptionPane.showMessageDialog(this, "khong duoc ma the loai khong vuot 10 ki tu!");
            } else {
                addData();
            }
        } else {
            JOptionPane.showMessageDialog(this, "khong duoc de trong field !");
        }
        loadData();
        clearField();
    }//GEN-LAST:event_btnThemActionPerformed

